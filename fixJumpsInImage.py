# Encuentra los puntos más cercanos entre cada par de componentes conexas
def find_closest_points_between_components(labeled_array, num_features):
    closest_points = {}  # Almacena los pares de puntos más cercanos entre componentes
    # Itera sobre cada componente conexa
    for i in range(1, num_features + 1):
        component_points = np.argwhere(labeled_array == i)  # Puntos de la componente actual
        min_distance = np.inf  # Inicializa la menor distancia con infinito
        closest_pair = None  # Para almacenar el par de puntos más cercano
        # Compara con otras componentes
        for j in range(1, num_features + 1):
            if i == j:  # Salta la comparación con sí misma
                continue
            other_component_points = np.argwhere(labeled_array == j)  # Puntos de la otra componente
            # Calcula la distancia a cada punto de la otra componente y guarda el más cercano
            for point in component_points:
                distances = np.sqrt(np.sum((point - other_component_points)**2, axis=1))
                nearest_distance = np.min(distances)
                if nearest_distance < min_distance:
                    min_distance = nearest_distance
                    closest_point = other_component_points[np.argmin(distances)]
                    closest_pair = (point, closest_point)
        if closest_pair:
            closest_points[i] = closest_pair  # Guarda el par más cercano encontrado
    return closest_points

# Conecta dos puntos en la matriz dibujando una línea entre ellos
def connect_points(matrix, point1, point2):
    x0, y0 = point1
    x1, y1 = point2
    dx = abs(x1 - x0)
    dy = -abs(y1 - y0)
    sx = 1 if x0 < x1 else -1
    sy = 1 if y0 < y1 else -1
    err = dx + dy
    while True:
        matrix[x0, y0] = 1  # Marca el punto en la matriz
        if x0 == x1 and y0 == y1:
            break  # Termina si alcanza el punto final
        e2 = 2 * err
        if e2 >= dy:
            err += dy
            x0 += sx
        if e2 <= dx:
            err += dx
            y0 += sy

# Conecta cada componente con su punto más cercano en otra componente
def connect_components(matrix):
    labeled_array, num_features = label(matrix)  # Etiqueta las componentes conexas
    while num_features > 2:  # Mientras haya más de una componente (2 es porque el background es una componente)
        print(f"Componentes restantes: {num_features - 1}")  # Imprime el número de componentes restantes
        closest_points = find_closest_points_between_components(labeled_array, num_features)  # Encuentra puntos más cercanos
        # Conecta los puntos más cercanos entre componentes
        for _, (point1, point2) in closest_points.items():
            connect_points(matrix, tuple(point1), tuple(point2))
        labeled_array, num_features = label(matrix)  # Etiqueta las componentes conexas
    return matrix



# For each txt in ./postScript/toFix folder fix the jumps in the image and save it in ./postScript/fixed folder
import os

# Take into account the txt file has lines and spaces between the numbers
for filename in os.listdir('./postScript/toFix'):
    if filename.endswith(".txt"):
        with open(f'./postScript/toFix/{filename}', 'r') as file:
            lines = file.readlines()
            matrix = np.array([[int(num) for num in line.split()] for line in lines])
            connected_matrix = connect_components(matrix)
            with open(f'./postScript/fixed/{filename}', 'w') as fixed_file:
                for row in connected_matrix.tolist():
                    fixed_file.write(' '.join(map(str, row)) + '\n')
    else:
        continue


